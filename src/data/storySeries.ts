// Mapping để user collection có thể track series
export const getSeriesProgress = (seriesId: string, collectedCards: string[]) => {
  const series = storySeries.find(s => s.id === seriesId);
  if (!series) return { collected: 0, total: 0, cards: [] };
  
  const collectedInSeries = series.cards.filter(cardId => collectedCards.includes(cardId));
  return {
    collected: collectedInSeries.length,
    total: series.totalCards,
    cards: collectedInSeries,
    isComplete: collectedInSeries.length === series.totalCards
  };
};
